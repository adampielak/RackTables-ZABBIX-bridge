function saveFile(size, scene) {
	var zip = new JSZip();
	var name = row_name;
	// fdp file content
	var fdp = createFDP( name );
	zip.file( name + ".fdp", fdp );
	// xml file content
	var xml = createXML( size, scene );
	zip.file( name + ".xml", xml );
	var content = zip.generate( { type:"blob" } );
	// see FileSaver.js
	saveAs( content, name + ".zip" );
}

